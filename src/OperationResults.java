public class OperationResults
{
    public static void main(String[] args)
    {
        int a1,a2,a3,a4;
        //First operation
        System.out.println("1st value:5");
        System.out.println("2nd value:8");
        System.out.println("3rd value:6");
        a1=-5+8*6;
        System.out.print("a1=-5=8*6");
        System.out.println("="+ a1);
        System.out.println();

        //Second operation
        System.out.println("1st value:55");
        System.out.println("2nd value:9");
        System.out.println("3rd value:9");
        a2=(55+9)%9;
        System.out.print("a2=(55+9)%9");
        System.out.println("="+ a2);
        System.out.println();

        //Third operation
        System.out.println("1st value:20");
        System.out.println("2nd value:3");
        System.out.println("3rd value:5");
        System.out.println("4th value:8");
        a3=20+-3*5/8;
        System.out.print("a3=20+-3*5/8");
        System.out.println("="+ a3);
        System.out.println();

        //Fourth operation
        System.out.println("1st value:5");
        System.out.println("2nd value:15");
        System.out.println("3rd value:3");
        System.out.println("4th value:2");
        System.out.println("5th value:8");
        System.out.println("6th value:3");
        a4=5+15/3*2-8%3;
        System.out.print("a4=5+15/3*2-8%3");
        System.out.println("="+ a4);

    }
}
